package pers.crazymouse.exercises;

import pers.crazymouse.exercises.myclass.Student;

public class Ex8_3 {
    public static void main(String[] args) {
        Student stu[] = new Student[8];
        char[][] answers = {{'A', 'B', 'A', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'D', 'B', 'A', 'B', 'C', 'A', 'E', 'E', 'A', 'D'},
                {'E', 'D', 'D', 'A', 'C', 'B', 'E', 'E', 'A', 'D'},
                {'C', 'B', 'A', 'E', 'D', 'C', 'E', 'E', 'A', 'D'},
                {'A', 'B', 'D', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'B', 'B', 'E', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'B', 'B', 'A', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'E', 'B', 'E', 'C', 'C', 'D', 'E', 'E', 'A', 'D'}};

        char[] keys = {'D', 'B', 'D', 'C', 'C', 'D', 'A', 'E', 'A', 'D'};

        for (int i = 0; i < answers.length; i++) {
            int correctCount = 0;
            for (int j = 0; j < answers[i].length; j++) {
                if (answers[i][j] == keys[j])
                    correctCount++;
            }
            stu[i] = new Student(i + "", correctCount);
        }
        java.util.Arrays.sort(stu, (o1, o2) -> o1.getScore() < o2.getScore() ? 1 : -1);
        for (int i = 0; i < 8; i++)
            System.out.println("Student " + stu[i].getName() + "'s correct count is " + (int) stu[i].getScore());
    }
}
